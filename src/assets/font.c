/*
 * Get font data index for a character
 */
static s32 font_index(char ch) {
    if (ch >= 'A' && ch <= 'Z') return (ch - 'A') + 4;
    if (ch >= 'a' && ch <= 'z') return (ch - 'a') + 30;
    if (ch >= '0' && ch <= '9') return (ch - '0') + 56;
    if (ch == ' ') return 0;
    if (ch == '!') return 1;
    if (ch == ':') return 2;
    if (ch == '-') return 66;
    if (ch == '.') return 67;
    if (ch == '/') return 68;
    return 0;  /* Default to space */
}

